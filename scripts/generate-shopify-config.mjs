import { promises as fs } from "node:fs";
import path from "node:path";

const rootDir = process.cwd();
const envFileCandidates = [".env.local", ".env"];

function parseEnvFile(contents) {
  const values = {};

  for (const rawLine of contents.split(/\r?\n/)) {
    const line = rawLine.trim();

    if (!line || line.startsWith("#")) {
      continue;
    }

    const separatorIndex = line.indexOf("=");

    if (separatorIndex === -1) {
      continue;
    }

    const key = line.slice(0, separatorIndex).trim();
    const value = line.slice(separatorIndex + 1).trim().replace(/^['"]|['"]$/g, "");
    values[key] = value;
  }

  return values;
}

async function loadEnvironment() {
  const merged = { ...process.env };

  for (const fileName of envFileCandidates) {
    const filePath = path.join(rootDir, fileName);

    try {
      const contents = await fs.readFile(filePath, "utf8");
      Object.assign(merged, parseEnvFile(contents), process.env);
    } catch (error) {
      if (error && typeof error === "object" && "code" in error && error.code === "ENOENT") {
        continue;
      }

      throw error;
    }
  }

  return merged;
}

function requiredValue(environment, key) {
  const value = environment[key]?.trim();

  if (!value) {
    throw new Error(
      `Variabile mancante: ${key}. Impostala in .env.local oppure nell'ambiente prima di generare shopify.app.toml.`,
    );
  }

  return value;
}

function booleanValue(environment, key, fallback) {
  const value = environment[key];

  if (value === undefined || value === "") {
    return fallback;
  }

  return value === "true";
}

function normalizeBaseUrl(value) {
  const url = new URL(value);
  return url.toString().replace(/\/$/, "");
}

function buildToml(environment) {
  const appUrl = normalizeBaseUrl(requiredValue(environment, "NEXT_PUBLIC_APP_URL"));
  const clientId = requiredValue(environment, "SHOPIFY_API_KEY");
  const name = environment.SHOPIFY_APP_NAME?.trim() || "Affinity";
  const handle = environment.SHOPIFY_APP_HANDLE?.trim() || "affinity";
  const embedded = booleanValue(environment, "SHOPIFY_APP_EMBEDDED", false);
  const useLegacyInstallFlow = booleanValue(
    environment,
    "SHOPIFY_USE_LEGACY_INSTALL_FLOW",
    true,
  );
  const automaticallyUpdateUrlsOnDev = booleanValue(
    environment,
    "SHOPIFY_CLI_AUTO_UPDATE_URLS",
    true,
  );
  const scopes =
    environment.SHOPIFY_SCOPES?.trim() ||
    "read_products,read_content,read_discounts,write_discounts,read_orders";
  const apiVersion = environment.SHOPIFY_API_VERSION?.trim() || "2025-10";
  const webhookUri = environment.SHOPIFY_WEBHOOK_URI?.trim() || "/api/webhooks/shopify";
  const redirectUrls = [`${appUrl}/api/shopify/callback`];
  const devStoreUrl = environment.SHOPIFY_DEV_STORE_URL?.trim() || "";

  const lines = [
    "# Generated by scripts/generate-shopify-config.mjs",
    `name = "${name}"`,
    `client_id = "${clientId}"`,
    `application_url = "${appUrl}"`,
    `embedded = ${embedded ? "true" : "false"}`,
    `handle = "${handle}"`,
    "",
    "[access_scopes]",
    `scopes = "${scopes}"`,
    `use_legacy_install_flow = ${useLegacyInstallFlow ? "true" : "false"}`,
    "",
    "[auth]",
    "redirect_urls = [",
    ...redirectUrls.map((url) => `  "${url}",`),
    "]",
    "",
    "[webhooks]",
    `api_version = "${apiVersion}"`,
    "",
    "[[webhooks.subscriptions]]",
    'topics = ["app/uninstalled"]',
    `uri = "${webhookUri}"`,
    "",
    "[[webhooks.subscriptions]]",
    'topics = ["orders/paid"]',
    `uri = "${webhookUri}"`,
    "",
    "[[webhooks.subscriptions]]",
    'topics = ["discounts/update"]',
    `uri = "${webhookUri}"`,
    "",
    "[build]",
    `automatically_update_urls_on_dev = ${automaticallyUpdateUrlsOnDev ? "true" : "false"}`,
  ];

  if (devStoreUrl) {
    lines.push(`dev_store_url = "${devStoreUrl}"`);
  }

  return `${lines.join("\n")}\n`;
}

async function main() {
  const environment = await loadEnvironment();
  const toml = buildToml(environment);
  const targetPath = path.join(rootDir, "shopify.app.toml");

  await fs.mkdir(path.dirname(targetPath), { recursive: true });
  await fs.writeFile(targetPath, toml, "utf8");

  const appUrl = normalizeBaseUrl(environment.NEXT_PUBLIC_APP_URL || "http://localhost:3000");
  console.log(`shopify.app.toml generato in ${targetPath}`);
  console.log(`Application URL: ${appUrl}`);

  if (/localhost|127\.0\.0\.1/i.test(appUrl)) {
    console.warn(
      "Attenzione: l'application_url punta a un host locale. Per un'installazione reale usa un URL HTTPS pubblico oppure lascia attivo SHOPIFY_CLI_AUTO_UPDATE_URLS=true durante shopify app dev.",
    );
  }

  if (!booleanValue(environment, "SHOPIFY_APP_EMBEDDED", false)) {
    console.warn(
      "Configurazione embedded=false: coerente con l'attuale auth flow server-side. Abilita embedded=true solo dopo aver introdotto App Bridge e session token.",
    );
  }
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error));
  process.exitCode = 1;
});
